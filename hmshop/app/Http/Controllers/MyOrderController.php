     */
    public function index()
    {
        $user = auth()->user();
        
        $order_list = Order::where('user_id', $user->id)->orderby('order_id', 'desc')->get();
        if (is_null($order_list)){
            return response()->json(['message'=>'You have not made any purchases!'], 400);
        }
        return response()->json($order_list, 200);
    }

